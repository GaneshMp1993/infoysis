package com.project.school;

public class School {
	private int id;
	private String name;
	private String location;
	private long cantactNumber;
	private boolean isCbscIsThere;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public long getCantactNumber() {
		return cantactNumber;
	}
	public void setCantactNumber(long cantactNumber) {
		this.cantactNumber = cantactNumber;
	}
	public boolean isCbscIsThere() {
		return isCbscIsThere;
	}
	public void setCbscIsThere(boolean isCbscIsThere) {
		this.isCbscIsThere = isCbscIsThere;
	}

}
